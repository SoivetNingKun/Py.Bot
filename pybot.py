def chatbot_response(user_input):
    for keyword in responses:
        if keyword in user_input:
            import random
            return random.choice(responses[keyword])
    return "我不太明白你的意思呢。"

print("Hello!我是PyBot，你的人机话唠🤖！想跟我聊什么呢？")
