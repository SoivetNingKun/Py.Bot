# 新增：脏话检测函数（先判断有没有脏话，再处理对话）
def check_swear_word(user_input):
    # 遍历脏话库，只要输入里包含任何一个脏话，就返回True（有脏话）
    for word in SwearWords:
        if word in user_input:
            return True
    return False

